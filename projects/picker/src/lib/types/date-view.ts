/**
 * An enum that identifies the different views of the calendar view.
 */
export enum DateView {
  /**
   * Shows the all days of the month.
   */
  MONTH = 'month',
  /**
   * Shows the all months of the year.
   */
  YEAR = 'year',
  /**
   * Shows multiple years numbers.
   */
  MULTI_YEARS = 'multi-years'
}
